#include "../includes/minishell.h"

int	is_parent(char c)
{
	return (c == '"' || c == 39);
}

int	op_len(const char *s)
{
	if (!s || !*s)
		return (0);
	if (s[0] == '<' && s[1] == '<')
		return (2);
	if (s[0] == '>' && s[1] == '>')
		return (2);
	if (s[0] == '<' || s[0] == '>' || s[0] == '|')
		return (1);
	return (0);
}
